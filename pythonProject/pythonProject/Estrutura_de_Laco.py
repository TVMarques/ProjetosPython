#COMANDO DE REPETIÇÃO#
print('Início do Programa')
#Para escrever um comando condicional corretamente, é preciso:
cont = 1#Ter uma condição inicial
while cont <= 10:#Ter uma condição de continuidade do laço
    print(cont) #Assim, imprime na tela o valor de cont inicial mais o incremento. Isto é aquilo o que o laço faz.
    cont = cont + 1#Deve se ter uma ação que é feita a cada repetição sobre o controle do laço.
print('Fim do Programa')